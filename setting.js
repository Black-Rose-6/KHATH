function profile(elem){
    document.getElementsByClassName("info")[0].style.display = "grid";
    let info_class = document.getElementsByClassName("data_info");
    for (let i = 0 ; i < info_class.length ; i++ )
    {
        info_class[i].style.display = "grid";
    }
    document.getElementsByClassName("account")[0].style.display = "none";
    let account_class = document.getElementsByClassName("data_account");
    for (let i = 0 ; i < account_class.length ; i++ )
    {
        account_class[i].style.display = "none";
    }
    document.getElementsByClassName("security")[0].style.display = "none";
    let security_class = document.getElementsByClassName("data_security");
    for (let i = 0 ; i < security_class.length ; i++ )
    {
        security_class[i].style.display = "none";
    }
    document.getElementsByClassName("notification")[0].style.display = "none";
    let notification_class = document.getElementsByClassName("data_notification");
    for (let i = 0 ; i < notification_class.length ; i++ )
    {
        notification_class[i].style.display = "none";
    }
    document.getElementsByClassName("accessibility")[0].style.display = "none";
    let accessibility_class = document.getElementsByClassName("data_accessibility");
    for (let i = 0 ; i < accessibility_class.length ; i++ )
    {
        accessibility_class[i].style.display = "none";
    }
    elem.classList.toggle("tabactive")
}

